package logic;

public class Move {

    int diceRoll;
    int startPos;
    int endPos;
    char piece;

    public Move(int diceRoll, int startPos, int endPos) {
        this.diceRoll = diceRoll;
        this.startPos = startPos;
        this.endPos = endPos;
        this.piece = piece;
    }



}
